let numero = 90
let numero2 = 45

//Comparar se um valor é igual ao outro 
console.log(numero === numero2) //FALSE
console.log('Leonardo' === 'Gabriel') //FALSE

//Comparar se um é diferente do outro
console.log(numero !== numero2) //TRUE

//Comparar se um é maior que o outro
console.log(numero > numero2) //TRUE

//Comparar se um é maior OU igual ao outro
console.log(numero >= numero2) //TRUE

//Comparar se um é menor que o outro
console.log(numero < numero2) //FALSE

//Comparar se um é menor OU igual ao outro
console.log(numero <= numero2) //FALSE

